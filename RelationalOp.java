class RelationalOp
{
	public static void main(String[] args)
	{
		int a = 18;
		int b = 10;
		//Equalto
		boolean c = a==b; //false
		//NotEqualto
		boolean d = a!=b; //true
		//Greaterthan
		boolean e = b>a;  //false
		//Lessthan
		boolean f = a<19;  //true
		//Greaterthan or Equalto
		boolean g = b>=11; //false
		//Lessthan or Equalto
		boolean h = a<=18; //true
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
	}
}
